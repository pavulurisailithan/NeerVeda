let currentLang = 'en';

function switchLanguage(lang) {
    currentLang = lang;
    const langText = document.getElementById('langText');
    if (langText) langText.textContent = lang === 'te' ? 'తె' : 'EN';
    
    const dropdown = document.getElementById('langDropdown');
    if (dropdown) dropdown.classList.add('hidden');
    
    // Translate elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = (lang === 'te' && translations.te[key]) ? translations.te[key] : key;
    });
    
    // Translate common elements by text content
    translateCommonElements(lang);
    
    // Store language preference
    localStorage.setItem('preferredLanguage', lang);
}

function translateCommonElements(lang) {
    if (lang !== 'te') return;
    
    // Translate navigation items
    const navItems = {
        'Home': 'మొదటి పేజీ',
        'Dashboard': 'డాష్బోర్డ్',
        'Alerts': 'అలర్ట్లు',
        'Fields': 'పొలాలు',
        'Seasons': 'ఋతువులు',
        'Irrigation': 'నీటిపారుదల'
    };
    
    document.querySelectorAll('nav a').forEach(link => {
        const text = link.textContent.trim();
        if (navItems[text]) {
            link.textContent = navItems[text];
        }
    });
    
    // Translate buttons
    const buttonTranslations = {
        'Get Started': 'ప్రారంభించండి',
        'Add Field': 'పొలం జోడించండి',
        'Refresh': 'రిఫ్రెష్',
        'Settings': 'సెట్టింగ్లు',
        'Filter': 'ఫిల్టర్',
        'Export': 'ఎగుమతి',
        'Save': 'సేవ్',
        'Cancel': 'రద్దు చేయండి',
        'Update': 'అప్డేట్',
        'View Details': 'వివరాలు చూడండి'
    };
    
    document.querySelectorAll('button').forEach(button => {
        const text = button.textContent.trim();
        if (buttonTranslations[text]) {
            button.textContent = buttonTranslations[text];
        }
    });
    
    // Translate headings
    const headingTranslations = {
        'Field Management': 'పొలం నిర్వహణ',
        'Smart Irrigation Control': 'స్మార్ట్ నీటిపారుదల నియంత్రణ',
        'Alert Management Center': 'హెచ్చరిక నిర్వహణ కేంద్రం',
        'Seasonal Crop Intelligence': 'కాలానుగుణ పంట తెలివితేటలు',
        'Analytics Dashboard': 'విశ్లేషణ డాష్బోర్డ్',
        'Crop Recommendations': 'పంట సిఫార్సులు'
    };
    
    document.querySelectorAll('h1, h2, h3').forEach(heading => {
        const text = heading.textContent.trim();
        Object.keys(headingTranslations).forEach(key => {
            if (text.includes(key)) {
                heading.textContent = heading.textContent.replace(key, headingTranslations[key]);
            }
        });
    });
}

function loadLanguagePreference() {
    const savedLang = localStorage.getItem('preferredLanguage');
    if (savedLang) {
        switchLanguage(savedLang);
    }
}

function initLanguageDropdown() {
    const langBtn = document.getElementById('langBtn');
    const langDropdown = document.getElementById('langDropdown');
    
    if (langBtn && langDropdown) {
        langBtn.addEventListener('click', () => langDropdown.classList.toggle('hidden'));
        document.addEventListener('click', (e) => {
            if (!e.target.closest('#langBtn') && !e.target.closest('#langDropdown')) {
                langDropdown.classList.add('hidden');
            }
        });
    }
}

document.addEventListener('DOMContentLoaded', () => {
    initLanguageDropdown();
    loadLanguagePreference();
});